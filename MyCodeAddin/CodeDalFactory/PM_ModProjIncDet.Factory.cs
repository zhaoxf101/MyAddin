namespace Common.DalFactory
{
	public class PM_ModProjIncDetFactory:Common.Business.PM_ModProjIncDet
    {
        public static Common.Business.PM_ModProjIncDet Fetch(PM_ModProjIncDet data)
        {
            Common.Business.PM_ModProjIncDet item = (Common.Business.PM_ModProjIncDet)Activator.CreateInstance(typeof(Common.Business.PM_ModProjIncDet));
            //using (ObjectFactory.BypassPropertyChecks(item))
            {
				                item.Client = data.Client;
				                item.ProjCode = data.ProjCode;
				                item.IncDetCode = data.IncDetCode;
							}
			ObjectFactory.MarkAsChild(item);
            ObjectFactory.MarkOld(item);
            return item;
        }

		private void DataPortal_Fetch(Common.Business.LambdaExpression lambda)
        {
            using (var ctx = ContextManager<EFDataContext>.GetManager(Database.EFramework))
            {
				var exp = lambda.Resolve<PM_ModProjIncDet>();
                var i = (from p in ctx.DataContext.PM_ModProjIncDet.Where(exp)
                         select p).FirstOrDefault();
                if (i != null)
                {
										this.Client = i.Client;
										this.ProjCode = i.ProjCode;
										this.IncDetCode = i.IncDetCode;
					}
            }
        }
	}

	public class PM_ModProjIncDetsFactory : Common.Business.PM_ModProjIncDets
    {
        private void DataPortal_Fetch()
        {
            using (var ctx = ContextManager<EFDataContext>.GetManager(Database.EFramework))
            {
                var i = from p in ctx.DataContext.PM_ModProjIncDet
                        select PM_ModProjIncDetFactory.Fetch(p);
                this.RaiseListChangedEvents = false;
                this.IsReadOnly = false;
                this.AddRange(i);
                this.IsReadOnly = true;
                this.RaiseListChangedEvents = true;
            }
        }

		private void DataPortal_Fetch(Paging page)
