using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using EF;
using EF.Data;
using EF.Linq;
using EF.Server;
using EFramework.DalLinq;

namespace Common.DalFactory
{
	public class HR_JobTypeFactory:Common.Business.HR_JobType
    {
        public static Common.Business.HR_JobType Fetch(HR_JobType data)
        {
            Common.Business.HR_JobType item = (Common.Business.HR_JobType)Activator.CreateInstance(typeof(Common.Business.HR_JobType));
            //using (ObjectFactory.BypassPropertyChecks(item))
            {
				                item.JobTypeCode = data.JobTypeCode;
				                item.JobTypeName = data.JobTypeName;
				                item.Memo = data.Memo;
							}
			ObjectFactory.MarkAsChild(item);
            ObjectFactory.MarkOld(item);
            return item;
        }

		private void DataPortal_Fetch(Common.Business.LambdaExpression lambda)
        {
            using (var ctx = ContextManager<EFDataContext>.GetManager(Database.EFramework))
            {
				var exp = lambda.Resolve<HR_JobType>();
                var i = (from p in ctx.DataContext.HR_JobType.Where(exp)
                         select p).FirstOrDefault();
                if (i != null)
                {
										this.JobTypeCode = i.JobTypeCode;
										this.JobTypeName = i.JobTypeName;
										this.Memo = i.Memo;
					}
            }
        }
	}

	public class HR_JobTypesFactory : Common.Business.HR_JobTypes
    {
        private void DataPortal_Fetch()
        {
            using (var ctx = ContextManager<EFDataContext>.GetManager(Database.EFramework))
            {
                var i = from p in ctx.DataContext.HR_JobType
                        select HR_JobTypeFactory.Fetch(p);
                this.RaiseListChangedEvents = false;
                this.IsReadOnly = false;
                this.AddRange(i);
                this.IsReadOnly = true;
                this.RaiseListChangedEvents = true;
            }
        }

		private void DataPortal_Fetch(Paging page)
        {
            using (var ctx = ContextManager<EFDataContext>.GetManager(Database.EFramework))
            {
                var i = (from p in ctx.DataContext.HR_JobType
                        select HR_JobTypeFactory.Fetch(p)).Skip(page.StartIndex).Take(page.RowCount);
                this.RaiseListChangedEvents = false;
                this.IsReadOnly = false;
                this.AddRange(i);
                this.IsReadOnly = true;
                this.RaiseListChangedEvents = true;
            }
        }

        private void DataPortal_Fetch(PagigExpress page)
        {
            using (var ctx = ContextManager<EFDataContext>.GetManager(Database.EFramework))
            {
                var exp = page.Lambda.Resolve<HR_JobType>();
                var i = (from p in ctx.DataContext.HR_JobType.Where(exp)
                         select HR_JobTypeFactory.Fetch(p)).Skip(page.StartIndex).Take(page.RowCount);
                this.RaiseListChangedEvents = false;
                this.IsReadOnly = false;
                this.AddRange(i);
                this.IsReadOnly = true;
                this.RaiseListChangedEvents = true;
            }
        }

		private void DataPortal_Fetch(Common.Business.LambdaExpression lambda)
        {
            using (var ctx = ContextManager<EFDataContext>.GetManager(Database.EFramework))
            {
                var exp = lambda.Resolve<HR_JobType>();
                var i = from p in ctx.DataContext.HR_JobType.Where(exp)
                         select HR_JobTypeFactory.Fetch(p);
                this.RaiseListChangedEvents = false;
                this.IsReadOnly = false;
                this.AddRange(i);
                this.IsReadOnly = true;
                this.RaiseListChangedEvents = true;
            }
        }


    }
}
