using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using EF;
using EF.Data;
using EF.Linq;
using EF.Server;
using EFramework.DalLinq;

namespace Common.DalFactory
{
	public class EF_ConfigGroupFactory:Common.Business.EF_ConfigGroup
    {
        public static Common.Business.EF_ConfigGroup Fetch(EF_ConfigGroup data)
        {
            Common.Business.EF_ConfigGroup item = (Common.Business.EF_ConfigGroup)Activator.CreateInstance(typeof(Common.Business.EF_ConfigGroup));
            //using (ObjectFactory.BypassPropertyChecks(item))
            {
				                item.ConfigGroup = data.ConfigGroup;
				                item.AGBlock = data.AGBlock;
				                item.DText = data.DText;
				                item.ClientX = data.ClientX;
				                item.CreatedUser = data.CreatedUser;
				                item.CreatedDate = data.CreatedDate;
				                item.ChangedUser = data.ChangedUser;
				                item.ChangedDate = data.ChangedDate;
							}
			ObjectFactory.MarkAsChild(item);
            ObjectFactory.MarkOld(item);
            return item;
        }

		private void DataPortal_Fetch(Common.Business.LambdaExpression lambda)
        {
            using (var ctx = ContextManager<EFDataContext>.GetManager(Database.EFramework))
            {
				var exp = lambda.Resolve<EF_ConfigGroup>();
                var i = (from p in ctx.DataContext.EF_ConfigGroup.Where(exp)
                         select p).FirstOrDefault();
                if (i != null)
                {
										this.ConfigGroup = i.ConfigGroup;
										this.AGBlock = i.AGBlock;
										this.DText = i.DText;
										this.ClientX = i.ClientX;
										this.CreatedUser = i.CreatedUser;
										this.CreatedDate = i.CreatedDate;
										this.ChangedUser = i.ChangedUser;
										this.ChangedDate = i.ChangedDate;
					}
            }
        }
	}

	public class EF_ConfigGroupsFactory : Common.Business.EF_ConfigGroups
    {
        private void DataPortal_Fetch()
        {
            using (var ctx = ContextManager<EFDataContext>.GetManager(Database.EFramework))
            {
                var i = from p in ctx.DataContext.EF_ConfigGroup
                        select EF_ConfigGroupFactory.Fetch(p);
                this.RaiseListChangedEvents = false;
                this.IsReadOnly = false;
                this.AddRange(i);
                this.IsReadOnly = true;
                this.RaiseListChangedEvents = true;
            }
        }

		private void DataPortal_Fetch(Paging page)
        {
            using (var ctx = ContextManager<EFDataContext>.GetManager(Database.EFramework))
            {
                var i = (from p in ctx.DataContext.EF_ConfigGroup
                        select EF_ConfigGroupFactory.Fetch(p)).Skip(page.StartIndex).Take(page.RowCount);
                this.RaiseListChangedEvents = false;
                this.IsReadOnly = false;
                this.AddRange(i);
                this.IsReadOnly = true;
                this.RaiseListChangedEvents = true;
            }
        }

        private void DataPortal_Fetch(PagigExpress page)
        {
            using (var ctx = ContextManager<EFDataContext>.GetManager(Database.EFramework))
            {
                var exp = page.Lambda.Resolve<EF_ConfigGroup>();
                var i = (from p in ctx.DataContext.EF_ConfigGroup.Where(exp)
                         select EF_ConfigGroupFactory.Fetch(p)).Skip(page.StartIndex).Take(page.RowCount);
                this.RaiseListChangedEvents = false;
                this.IsReadOnly = false;
                this.AddRange(i);
                this.IsReadOnly = true;
                this.RaiseListChangedEvents = true;
            }
        }

		private void DataPortal_Fetch(Common.Business.LambdaExpression lambda)
        {
            using (var ctx = ContextManager<EFDataContext>.GetManager(Database.EFramework))
            {
                var exp = lambda.Resolve<EF_ConfigGroup>();
                var i = from p in ctx.DataContext.EF_ConfigGroup.Where(exp)
                         select EF_ConfigGroupFactory.Fetch(p);
                this.RaiseListChangedEvents = false;
                this.IsReadOnly = false;
                this.AddRange(i);
                this.IsReadOnly = true;
                this.RaiseListChangedEvents = true;
            }
        }


    }
}
